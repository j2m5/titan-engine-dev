/**
 * Общие GLSL-чанки модуля BlackHole
 *
 * BLACKBODY_GLSL используется и основным лензинг-шейдером (L0), и импостором
 * (L1): идентичность формулы — требование §8 спецификации, палитры уровней
 * обязаны совпадать на дистанции переключения LOD
 */
export const BLACKBODY_GLSL: string = /* glsl */ `
    // Аналитический планковский blackbody: CIE-аппроксимация локуса → XYZ →
    // linear sRGB, нормировка по максимальному каналу (яркость задаётся
    // отдельно интенсивностью)
    vec3 blackbody(float t) {
      t = clamp(t, 1500.0, 20000.0);
      float u = (0.860117757 + 1.54118254e-4 * t + 1.28641212e-7 * t * t)
              / (1.0 + 8.42420235e-4 * t + 7.08145163e-7 * t * t);
      float v = (0.317398726 + 4.22806245e-5 * t + 4.20481691e-8 * t * t)
              / (1.0 - 2.89741816e-5 * t + 1.61456053e-7 * t * t);
      float x = 3.0 * u / (2.0 * u - 8.0 * v + 4.0);
      float y = 2.0 * v / (2.0 * u - 8.0 * v + 4.0);
      vec3 xyz = vec3(x / y, 1.0, (1.0 - x - y) / y);
      vec3 rgb = mat3(
        3.2404542, -0.9692660, 0.0556434,
        -1.5371385, 1.8760108, -0.2040259,
        -0.4985314, 0.0415560, 1.0572252
      ) * xyz;
      rgb = max(rgb, vec3(0.0));
      return rgb / max(max(rgb.r, rgb.g), max(rgb.b, 1e-4));
    }
`
